<template>
  <v-app id="login" class="secondary">
    <v-main>
      <v-container fluid fill-height>
        <v-layout align-center justify-center>
          <v-flex xs12 sm8 md4 lg4>
            <v-card class="elevation-1 pa-3">
              <v-card-text>
                <div class="layout column align-center">
                  <img src="@/assets/avatar_user.png" alt="Fast Project Kit Admin" width="180" height="180">
                  <h1 class="flex my-4 primary--text">Fast Project Kit Admin Login</h1>
                </div>
                <v-form>
                  <v-text-field
                    append-icon="mdi-account"
                    name="login"
                    label="Login"
                    type="text"
                    v-model="email"
                    :error="error"
                    :rules="[rules.required]"/>
                  <v-text-field
                    :type="hidePassword ? 'password' : 'text'"
                    :append-icon="hidePassword ? 'mdi-eye-off' : 'mdi-eye'"
                    name="password"
                    label="Password"
                    id="password"
                    :rules="[rules.required]"
                    v-model="password"
                    :error="error"
                    @click:append="hidePassword = !hidePassword"/>
                </v-form>
              </v-card-text>
              <v-card-actions>
                <v-spacer></v-spacer>
                <v-btn block color="primary" @click="login" :loading="loading">Login</v-btn>
              </v-card-actions>
            </v-card>
          </v-flex>
        </v-layout>
      </v-container>
      <v-snackbar
        v-model="showResult"
        :timeout="2000"
        top>
        {{ result }}
      </v-snackbar>
    </v-main>
  </v-app>
</template>

<script>

export default {
  data() {
    return {
      loading: false,
      token: undefined,
      email: 'asd@asd.com',
      password: 'asd',
      hidePassword: true,
      error: false,
      showResult: false,
      result: '',
      rules: {
        required: value => !!value || 'Required.'
      }
    }
  },
  methods: {
    login() {
      if (!this.email || !this.password) {
        this.result = "Email and Password can't be null.";
        this.showResult = true;
        return;
      }
      
      let email = this.email
      let password = this.password
      this.$store.dispatch("login", {email, password}).then(() => {
          if (this.$store.getters.token) {
            this.$router.push({ name: 'Dashboard' });
          }
          else {
            this.error = true;
            this.result = "Email or Password is incorrect.";
            this.showResult = true;
          }
      })
      .catch(()=> {
          this.error = true;
            this.result = "Email or Password is incorrect.";
            this.showResult = true;
      }) 


     

    }
  }
}
</script>

<style scoped lang="css">
  #login {
    height: 50%;
    width: 100%;
    position: absolute;
    top: 0;
    left: 0;
    content: "";
    z-index: 0;
  }
</style>
